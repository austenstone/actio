#!/usr/bin/env node
// Compile every tests/fixtures/* into a committed reusable workflow so GitHub's
// real Actions engine validates Actio's emit. Each fixture becomes an
// `on: [workflow_call, workflow_dispatch]` workflow; the generated driver
// (e2e.yml) references all of them via `uses:` with `if: false`, so GitHub
// validates each emitted workflow at run-graph build (catching structural
// startup_failures that actionlint + the JSON schema miss) without burning
// runner minutes on the fixture bodies (which call missing scripts and can't
// run to green). A structurally-invalid emit aborts the whole driver run →
// red PR check. Valid emits → callers skipped → run succeeds.
//
// Usage:
//   node scripts/build-e2e-fixtures.mjs           # write files
//   node scripts/build-e2e-fixtures.mjs --check    # fail (exit 1) on drift

import { existsSync, mkdirSync, readdirSync, readFileSync, rmSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import { transpile } from "../packages/core/dist/index.js";
import { parseDocument } from "yaml";

const repoRoot = join(dirname(fileURLToPath(import.meta.url)), "..");
const fixturesDir = join(repoRoot, "tests", "fixtures");
const workflowsDir = join(repoRoot, ".github", "workflows");

const FIXTURE_PREFIX = "e2e-fixture-";

/** Generous permission union so no fixture's job-level permissions can exceed
 *  the caller cap and trip a permission-cap startup failure. */
const DRIVER_PERMISSIONS = {
  actions: "write",
  attestations: "write",
  checks: "write",
  contents: "write",
  deployments: "write",
  "id-token": "write",
  issues: "write",
  packages: "write",
  pages: "write",
  "pull-requests": "write",
  "security-events": "write",
  statuses: "write",
};

function fixtureHeader(name) {
  return [
    "# Code generated by the Actio E2E harness. DO NOT EDIT.",
    `# Source: tests/fixtures/${name}/input.actio.yml`,
    "# Regenerate: npm run build:e2e-fixtures",
    "",
    "",
  ].join("\n");
}

function driverHeader() {
  return [
    "# Code generated by the Actio E2E harness. DO NOT EDIT.",
    "# Source: scripts/build-e2e-fixtures.mjs",
    "# Regenerate: npm run build:e2e-fixtures",
    "",
    "",
  ].join("\n");
}

/** Discover runnable fixtures, skipping intentionally non-standard emits
 *  (options.json `validate: false`, e.g. the native-dependencies preview that
 *  emits a `dependencies:` key GitHub would reject). */
function discoverFixtures() {
  return readdirSync(fixturesDir, { withFileTypes: true })
    .filter((d) => d.isDirectory())
    .map((d) => d.name)
    .filter((name) => existsSync(join(fixturesDir, name, "input.actio.yml")))
    .map((name) => {
      const optionsPath = join(fixturesDir, name, "options.json");
      const options = existsSync(optionsPath)
        ? JSON.parse(readFileSync(optionsPath, "utf8"))
        : {};
      return { name, options };
    })
    .filter(({ options }) => options.validate !== false)
    .sort((a, b) => a.name.localeCompare(b.name));
}

/** A job that itself calls a reusable workflow (`uses:` at job level) can't be
 *  validated in isolation: GitHub resolves the nested ref at run-graph build,
 *  and our fixtures point at placeholder/external repos (e.g. org/repo/...@main)
 *  that don't exist, which would abort the whole driver run. Exclude them. */
function emittedHasJobLevelUses(doc) {
  const jobs = doc.get("jobs");
  if (!jobs || !Array.isArray(jobs.items)) return false;
  return jobs.items.some(
    (pair) => pair.value && typeof pair.value.get === "function" && pair.value.get("uses") != null,
  );
}

/** Transpile a fixture and rewrite its triggers to make it a reusable workflow.
 *  Returns { content } when e2e-eligible, or { skipped: reason } otherwise. */
function buildFixtureWorkflow(name, options) {
  const input = readFileSync(join(fixturesDir, name, "input.actio.yml"), "utf8");
  const result = transpile(input, { fileName: "input.actio.yml", header: false, ...options });
  const errors = result.diagnostics.filter((d) => d.severity === "error");
  if (!result.ok || errors.length > 0) {
    throw new Error(`fixture "${name}" failed to transpile:\n${JSON.stringify(errors, null, 2)}`);
  }

  const doc = parseDocument(result.yaml);
  if (emittedHasJobLevelUses(doc)) {
    return { skipped: "emits a reusable-workflow call (job-level uses:) GitHub can't resolve here" };
  }
  doc.set("on", doc.createNode({ workflow_call: {}, workflow_dispatch: {} }));
  return { content: fixtureHeader(name) + String(doc) };
}

/** Generate the driver that references every fixture workflow. */
function buildDriver(names) {
  const callers = names
    .map((name) =>
      [
        `  ${FIXTURE_PREFIX}${name}:`,
        "    needs: check",
        "    if: false",
        `    uses: ./.github/workflows/${FIXTURE_PREFIX}${name}.yml`,
      ].join("\n"),
    )
    .join("\n");

  const permissions = Object.entries(DRIVER_PERMISSIONS)
    .map(([scope, level]) => `  ${scope}: ${level}`)
    .join("\n");

  return `${driverHeader()}name: E2E (fixtures on real Actions)

# Proves GitHub's workflow engine accepts every Actio-emitted fixture. The
# fixture callers below are \`if: false\`, so GitHub validates each referenced
# reusable workflow at run-graph build then skips it (0 runner-minutes). A
# structurally-invalid emit aborts this whole run before any job → red check.
on:
  workflow_dispatch:
  schedule:
    - cron: "0 7 * * *"
  pull_request:
    paths:
      - "packages/core/**"
      - "packages/cli/**"
      - "tests/fixtures/**"
      - ".github/workflows/e2e*.yml"
      - "scripts/build-e2e-fixtures.mjs"

permissions:
${permissions}

jobs:
  check:
    runs-on: ubuntu-latest
    steps:
      - uses: actions/checkout@v4
      - uses: actions/setup-node@v4
        with:
          node-version: 22
      - run: npm ci
      - run: npm run build
      - run: npm run check:workflows
      - run: node scripts/build-e2e-fixtures.mjs --check
${callers}
`;
}

/** Compute every generated file as { relativePath: content }. */
function computeOutputs() {
  const fixtures = discoverFixtures();
  const outputs = new Map();
  const included = [];
  for (const { name, options } of fixtures) {
    const built = buildFixtureWorkflow(name, options);
    if (built.skipped) {
      console.warn(`⏭️  skip ${name}: ${built.skipped}`);
      continue;
    }
    outputs.set(`${FIXTURE_PREFIX}${name}.yml`, built.content);
    included.push(name);
  }
  outputs.set("e2e.yml", buildDriver(included));
  return outputs;
}

function listExistingGenerated() {
  return readdirSync(workflowsDir).filter(
    (f) => f === "e2e.yml" || (f.startsWith(FIXTURE_PREFIX) && f.endsWith(".yml")),
  );
}

function write(outputs) {
  mkdirSync(workflowsDir, { recursive: true });
  for (const stale of listExistingGenerated()) {
    if (!outputs.has(stale)) rmSync(join(workflowsDir, stale));
  }
  for (const [file, content] of outputs) {
    writeFileSync(join(workflowsDir, file), content);
  }
  console.log(`✅ wrote ${outputs.size} e2e workflow(s) to .github/workflows/`);
}

function check(outputs) {
  const drift = [];
  for (const [file, content] of outputs) {
    const path = join(workflowsDir, file);
    if (!existsSync(path)) drift.push(`missing: ${file}`);
    else if (readFileSync(path, "utf8") !== content) drift.push(`changed: ${file}`);
  }
  for (const existing of listExistingGenerated()) {
    if (!outputs.has(existing)) drift.push(`extra: ${existing}`);
  }
  if (drift.length > 0) {
    console.error("❌ e2e workflows are out of date. Run: npm run build:e2e-fixtures\n");
    console.error(drift.map((d) => `  - ${d}`).join("\n"));
    process.exit(1);
  }
  console.log(`✅ ${outputs.size} e2e workflow(s) up to date`);
}

const outputs = computeOutputs();
if (process.argv.includes("--check")) check(outputs);
else write(outputs);
